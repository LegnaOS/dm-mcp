-- 技能配置示例
-- 编码：GBK

local 配置 = {
    类型 = "技能",
    名称 = "示例技能",
    说明 = "这是一个示例技能",
    
    -- 图标
    图标 = "",  -- 资源标识
    
    -- 消耗
    魔法消耗 = 10,
    冷却时间 = 1000,  -- 毫秒
    
    -- 范围
    施法范围 = 5,
    作用范围 = 1,
    
    -- 事件
    事件 = {
        释放时 = function(技能, 释放者, 目标)
            调试输出(释放者.名称 .. " 释放了 " .. 技能.名称)
            
            -- 检查魔法值
            if 释放者.魔法值 < 技能.魔法消耗 then
                调试输出("魔法值不足")
                return false
            end
            
            -- 扣除魔法值
            释放者.魔法值 = 释放者.魔法值 - 技能.魔法消耗
            
            -- 在这里实现技能效果
            if 目标 then
                调试输出("对 " .. 目标.名称 .. " 造成伤害")
            end
            
            return true
        end,
        
        命中时 = function(技能, 释放者, 目标)
            调试输出(技能.名称 .. " 命中了 " .. 目标.名称)
        end
    }
}

return 配置
