-- 角色配置示例
-- 编码：GBK

local 配置 = {
    类型 = "角色",
    名称 = "示例角色",
    
    -- 基础属性
    生命值 = 100,
    魔法值 = 100,
    移动速度 = 5,
    
    -- 外观
    图片 = "",  -- 资源标识
    宽度 = 32,
    高度 = 48,
    
    -- 属性
    等级 = 1,
    经验值 = 0,
    
    -- 战斗属性
    攻击力 = 10,
    防御力 = 5,
    
    -- 事件
    事件 = {
        创建时 = function(角色)
            调试输出("角色创建: " .. 角色.名称)
        end,
        
        死亡时 = function(角色)
            调试输出("角色死亡: " .. 角色.名称)
        end,
        
        升级时 = function(角色, 新等级)
            调试输出(角色.名称 .. " 升级到 " .. 新等级)
        end,
        
        受伤时 = function(角色, 伤害, 攻击者)
            调试输出(角色.名称 .. " 受到 " .. 伤害 .. " 点伤害")
            return true
        end
    }
}

return 配置
